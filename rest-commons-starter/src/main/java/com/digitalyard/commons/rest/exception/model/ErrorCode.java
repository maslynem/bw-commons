package com.digitalyard.commons.rest.exception.model;

/**
 * Интерфейс-маркер для всех типов кодов ошибок в системе.
 * Все enum'ы с кодами ошибок должны реализовывать этот интерфейс.
 */
public interface ErrorCode {
    /**
     * Возвращает строковое представление кода ошибки.
     * Обычно это имя enum-константы.
     */
    String name();

    /**
     * Дефолтная реализация, возвращающая имя кода ошибки.
     * Может быть переопределена для кастомного форматирования.
     */
    default String getCode() {
        return name();
    }
}
